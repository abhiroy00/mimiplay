def main():
    """Main setup flow"""
    print("""
╔══════════════════════════════════════════════════════════════════════╗
║                                                                      ║
║                    MIMI AI QUICK START SETUP                         ║
║                                                                      ║
║  This will guide you through setting up your Mimi AI system         ║
║  with conversation memory capabilities                               ║
║                                                                      ║
╚══════════════════════════════════════════════════════════════════════╝
    """)
    
    print("Press Ctrl+C at any time to exit\n")
    
    try:
        # Check if we're in the right directory
        if not check_file_exists("app.py"):
            print("❌ Error: app.py not found!")
            print("Make sure you're in the mimiplay project directory")
            return
        
        # Step 1: Create .env
        if not create_env_file():
            print("\n❌ Setup failed at step 1")
            return
        
        # Step 2: Install dependencies
        install = input("\nInstall Python dependencies now? (y/n): ").lower()
        if install == 'y':
            if not install_dependencies():
                print("\n⚠️  Continuing anyway...")
        else:
            print("\nSkipped. Install later with: pip install -r requirements.txt")
        
        # Step 3: Test configuration
        if not test_setup():
            print("\n⚠️  Configuration issues detected")
            cont = input("Continue anyway? (y/n): ").lower()
            if cont != 'y':
                return
        
        # Step 4: Run tests
        run_tests()
        
        # Show next steps
        show_next_steps()
        
    except KeyboardInterrupt:
        print("\n\n⚠️  Setup interrupted by user")
        print("You can run this script again to continue setup")
    except Exception as e:
        print(f"\n❌ Setup failed: {e}")
        import traceback
        traceback.print_exc()

if __name__ == "__main__":
    main()
